import math
import datetime
import os

class Trig():
    valorPI = math.pi
            
    def senoPI(self):
        resultado = math.sin(self.valorPI)
        return resultado
    
    def cosenoPI(self):
        resultado = math.cos(self.valorPI)
        return resultado
    
    def tangentePI(self):
        resultado = math.tan(self.valorPI)
        return resultado
    
    


    

